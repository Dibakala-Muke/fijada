<script>
    document.addEventListener('DOMContentLoaded', function() {
        const carousel = document.getElementById('partnersCarousel');
        const prevButton = document.getElementById('prevButton');
        const nextButton = document.getElementById('nextButton');
        const slideIndicators = document.querySelectorAll('[data-slide-to]');
        let currentIndex = 0;
        const itemCount = document.querySelectorAll('#partnersCarousel > div').length;

        function updateCarousel() {
            const offset = -currentIndex * 100;
            carousel.style.transform = `translateX(${offset}%)`;

            // Mise à jour des indicateurs
            slideIndicators.forEach((indicator, index) => {
                indicator.classList.toggle('bg-fijada-blue', index === currentIndex);
                indicator.classList.toggle('w-6', index === currentIndex);
                indicator.classList.toggle('w-3', index !== currentIndex);
            });
        }

        // Navigation
        [prevButton, nextButton].forEach(btn => {
            btn.addEventListener('click', function() {
                currentIndex = (this === nextButton) ?
                    (currentIndex + 1) % itemCount :
                    (currentIndex - 1 + itemCount) % itemCount;
                updateCarousel();
            });
        });

        // Navigation via indicateurs
        slideIndicators.forEach(indicator => {
            indicator.addEventListener('click', () => {
                currentIndex = parseInt(indicator.dataset.slideTo);
                updateCarousel();
            });
        });
    });
</script>
